#把两个数字赋值
num_left = 5
num_right = 2
#数值类型的四则运算，加(+)、减(-)、乘(*)、除(/)、整除(//)、余(%)、幂(**)
print(num_left,'+',num_right,'=',num_left + num_right)
print(num_left,'-',num_right,'=',num_left - num_right)
print(num_left,'*',num_right,'=',num_left * num_right)
print(num_left,'/',num_right,'=',num_left / num_right)
print(num_left,'//',num_right,'=',num_left // num_right)
print(num_left,'%',num_right,'=',num_left % num_right)
print(num_left,'**',num_right,'=',num_left ** num_right)

