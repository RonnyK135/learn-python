#使用单引号，双引号，三个单引号或三个双引号引起来的一些字符
name = 'yueyong'
desc = "ma name is kk"
print('i\'m yueyong')
print("i\'m yueyong")
print('''i\'m yueyong''')

#特殊字符：“\（转义字符）、\r（回车）、\n（换行）、\t（tab键缩进）、\f（换页）”
print("i'm yueyong")
print('i\'m yueyong')
print('a\nb\tc')
print('a\\nb\\tc\\')  #转义自身\字符

#字符串类型运算，只有加（+）和乘（*）
print('abc' + 'def')
print('abc' * 2)
